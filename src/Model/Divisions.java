package Model;

public class Divisions {

	private String codeDivision;
	private String name;
	private int sumConditionDivision;

	public String getCodeDivision() {
		return codeDivision;
	}

	public void setCodeDivision(String codeDivision) {
		this.codeDivision = codeDivision;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSumConditionDivision() {
		return sumConditionDivision;
	}

	public void setSumConditionDivision(int sumConditionDivision) {
		this.sumConditionDivision = sumConditionDivision;
	}

	

}
